public class ForLoop {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {  // i++  == i + 1
            System.out.println("Вывод текста " + i);
        }
        for (int j = 10; j >= 1; j--) {  // i--  == i - 1
            System.out.println("Вывод текста " + j);
        }
    }
}