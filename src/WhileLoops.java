public class WhileLoops {
    public static void main(String[] args) {
        boolean s = 5 > 2; // условие на истину ! верное!
        boolean s1 = 5 > 7; // условие на истину ! не верное!
        boolean s2 = 43 >= 43; // условие на истину ! верное!
        boolean s3 = 7 == 8; // условие на истину ! не верное!
        boolean s4 = 77 == 77; // условие на истину ! верное!
        int age = 33;
        boolean my = age > 32;

        System.out.println(s);
        System.out.println(s1);
        System.out.println(s2);
        System.out.println(s3);
        System.out.println(s4);
        System.out.println(my);


    }
}
